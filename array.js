//array initialization
let arr = [2, 3, 1, 4, 5, 6, 7, 8, 9, 10];

//array methods
console.log(arr.length);

//push method to enter the element in array at end
arr.push(12);
console.log(arr.length);
console.log(arr);

//delete the element from starting
arr.shift();
console.log(arr);

//delete the element from last
arr.pop();
console.log(arr);

//add element at starting of array
arr.unshift(2);
console.log(arr);

//sort method : because it compares the string of array so 10
arr.sort();
console.log(arr);
